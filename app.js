const frozenWords = {}; // Track frozen words

// Function to pick a random word from a category
function getRandomWord(category) {
    const words = categories[category];
    const randomIndex = Math.floor(Math.random() * words.length);
    return words[randomIndex];
}

// Function to generate and display the art description
function generateArtDescription() {
    const artDescription = [];

    for (const category in categories) {
        let word = getRandomWord(category);

        // Check if the word is frozen
        if (frozenWords[category]) {
            word = frozenWords[category];
        }

        const wordElement = document.createElement("span");
        wordElement.textContent = word;
        wordElement.setAttribute("data-category", category);

        if (frozenWords[category]) {
            wordElement.classList.add("frozen");
        }

        wordElement.addEventListener("click", toggleFreeze);
        artDescription.push(wordElement);
    }

    const outputElement = document.getElementById("output");
    outputElement.innerHTML = ""; // Clear the previous content
    artDescription.forEach((element, index) => {
        if (index > 0) {
            outputElement.appendChild(document.createTextNode(", "));
        }
        outputElement.appendChild(element);
    });
}

// Function to freeze/unfreeze a word
function toggleFreeze(event) {
    const wordElement = event.target;
    const category = wordElement.getAttribute("data-category");
    const word = wordElement.textContent;

    if (frozenWords[category] === word) {
        // Unfreeze the word
        delete frozenWords[category];
        wordElement.classList.remove("frozen");
    } else {
        // Freeze the word
        frozenWords[category] = word;
        wordElement.classList.add("frozen");
    }
}

// Event listener for the "Generate" button
document.getElementById("generateButton").addEventListener("click", generateArtDescription);

// Initial generation
generateArtDescription();